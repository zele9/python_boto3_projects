#Lists are used to store multiple values in a variable like arrays
test_var_list = ["Ataba","Karen","Daisy","Api"]
print(test_var_list)
print(test_var_list[2])
print(test_var_list[0:3])
#+++++++++++++++++++++++++++++++++++++++++++++++++++++++
#Concept of Dict, is like a list but starts with {} rather than []
#Also, unlike list whose values are referred using indexes [0], its values are referred using key: value pairs
test_var_dict = {"Course_Name":"Python", "Duration":"25hrs", "Time":"8AM"}
print(test_var_dict)
print(test_var_dict["Course_Name"])
print(test_var_dict["Duration"])